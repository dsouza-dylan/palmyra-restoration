// ── Build flat array for D3 line charts (uses real data) ──
function buildLineData(organism) {
  const rows = [];
  TREATMENTS.forEach(trt => {
    REAL_DATES.forEach((date, i) => {
      rows.push({
        date,
        treatment: trt,
        mean: REAL_MEANS[organism][trt][i],
        se:   REAL_SE[organism][trt][i],
        organism,
      });
    });
  });
  return rows;
}

// ── Build stacked bar data for community chart (uses real data + real dates) ──
function buildStackedData(treatment) {
  return REAL_DATES.map((date, i) => {
    const row = { date, treatment };
    STACK_ORDER.forEach(org => {
      row[org] = REAL_MEANS[org][treatment][i];
    });
    return row;
  });
}

// Human-readable date label
function dateLabel(d) {
  return d.toLocaleString('en-US', { month: 'short', year: '2-digit', timeZone: 'UTC' })
          .replace(' ', ' \'');
}
